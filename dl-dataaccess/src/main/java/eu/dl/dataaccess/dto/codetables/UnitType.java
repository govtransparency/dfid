package eu.dl.dataaccess.dto.codetables;

/**
 * Unit type.
 */
public enum UnitType {
    // VOLUME
    /**
     * Cubic centimeters (cm^3).
     */
    CUBIC_CENTIMETERS,
    /**
     * Cubic meters (m^3).
     */
    CUBIC_METERS,
    /**
     * Litres.
     */
    LITRES,
    /**
     * Millilitres.
     */
    MILLILITRES,

    // SURFACE
    /**
     * Square meters (m^2).
     */
    SQUARE_METERS,
    /**
     * Square centimeters (cm^2).
     */
    SQUARE_CENTIMETERS,
    /**
     * Square millimeters (mm^2).
     */
    SQUARE_MILLIMETERS,
    /**
     * Hectares.
     */
    HECTARES,

    // LONG
    /**
     * Yards.
     */
    YARDS,
    /**
     * Meters.
     */
    METERS,
    /**
     * Centimeters.
     */
    CENTIMETERS,
    /**
     * Kilometers.
     */
    KILOMETERS,
    /**
     * Inches.
     */
    INCHES,

    // WEIGHT
    /**
     * Grams.
     */
    GRAMS,
    /**
     * Kilograms.
     */
    KILOGRAMS,
    /**
     * Tons.
     */
    TONS,
    /**
     * Milligrams.
     */
    MILLIGRAMS,

    // TIME
    /**
     * Months.
     */
    MONTHS,
    /**
     * Day.
     */
    DAYS,
    /**
     * Year.
     */
    YEARS,
    /**
     * Hours.
     */
    HOURS,
    /**
     * Minutes.
     */
    MINUTES,
    /**
     * Seconds.
     */
    SECONDS,

    // MISCELLANEOUS
    /**
     * Pieces.
     */
    PIECES,
    /**
     * Rations.
     */
    RATIONS,
    /**
     * Other.
     */
    OTHER
}
